// %d is a integer formate specifire
// %f is float formate specifire
// %c is character formate specifire
#include<stdio.h>
void main()
{
   printf("integer value = %d\n",125);
   printf("integer value = %d\n",12+8);
   printf("float value = %f\n",12.78);
   printf("float value = %.3f\n",12.78);
   printf("character value = %c\n",'K');  	
}
